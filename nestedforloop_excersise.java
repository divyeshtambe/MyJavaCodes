package demoCode;

public class nestedforloop_excersise
{

	public static void main(String[] args)
	{
		int a=1;
		int b=1;
		
		for(int i=1;i<5;i++)
		{
			
			for(int j=b;j<5;j++)
			{
				System.out.print(a+" ");
				a++;
			}
			b++;
			System.out.println("\n");
			
			
		}
		
		
	}

}
